<template lang="pug">
  el-select(v-model="selectedProvince" clearable style="width: 100%" filterable placeholder="Tá»‰nh/TP" @change="_sendSelectedObjectToParent")
    el-option(
      v-for="item in provinces"
      :key="item.code"
      :label="item.name_with_type"
      :value="item.code")
</template>

<script>
import originProvinces from './province.json';

export default {
  name: 'province-select',
  model: {
    prop: 'province',
    event: 'change'
  },
  props: {
    province: String
  },
  data() {
    return {
      provinces: JSON.parse(JSON.stringify(originProvinces)),
      selectedProvince: ''
    };
  },
  methods: {
    _sendSelectedObjectToParent() {
      this.$emit('change', this.selectedProvince);
    }
  }
}
</script>
